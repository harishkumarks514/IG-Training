// Java program to illustrate the concept of single inheritance

package Inheritance;

import java.io.*;
import java.lang.*;
import java.util.*;

public class One {

	public void print_geek() {
		System.out.println("Geeks");
	}
}

class Two extends One {
	
	public void print_for()
	{
		System.out.println("for");
	}
	

}





